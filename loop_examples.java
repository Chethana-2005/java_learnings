class ForLoopExample {
    public static void main(String[] args) {
        for (int i = 0; i<=10; i++) {
            System.out.println(i);
        }
    }
}

class ForLoopCountDown {
    public static void main(String[] args) {
        for (int i = 2; i <= 50; i=i+2) {
            System.out.println(i);
        }
    }
}